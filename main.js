function burger1() {
    const zakaz = prompt("Nechta set zakaz qilasan");

    if (zakaz === null || zakaz.trim() === "" || isNaN(zakaz) || zakaz <=0){
        alert("Iltimos, to'gri miqdori kiriting.");
        return;
    }
    const rezult = zakaz * 45000;
    const isConfirmed = confirm(`${zakaz} te set ${rezult} so'm bo'ladi. Zakaz qilasizmi`);

    if (isConfirmed) {
        alert("Zakaz qabul qilindi!")
    }else{
        alert("Zakaz bekor qilindi!")
    }
}
